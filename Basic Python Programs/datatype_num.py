# number datatype
n1 = 10
print("The value of n1 is:", n1)
print(type(n1))
print("IS n1 int?", isinstance(10, int))

n2 = 10.5
print("The value of n2 is:", n2)
print(type(n2))
print("IS n2 Float?", isinstance(10.5, float))

n3 = 2+3j
print("The value of n3 is:", n3)
print(type(n3))
print("IS n3 complex?", isinstance(2+3j, complex))
