#String Datatype

name="Aarohi Rakeshbhai Mistry"

#prints complete string
print("The name is :",name)

#prints the first character of the string
print(name[0])

#prints the strings starting from 2nd index to 5th index
print(name[2:6])

#prints the string startinf from 2nd index
print(name[2:])

#prints the string two times
print(name * 2)

#prints concatenated string
print("Hello " + name)


